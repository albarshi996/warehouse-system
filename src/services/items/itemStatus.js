/**
 * حالة الصنف — منطق خالص بلا شبكة ولا Firebase.
 *
 * مفصول عن `itemService.js` عمدًا ليُختبر وحده: تفسير هذا العمود خطأً يقلب
 * الكتالوج كلّه بضربة واحدة، فيستحقّ اختبارًا لا يحتاج سحابةً ليعمل.
 */

/** ما يعنيه «موقوف» في عمود الحالة بالشيت. */
const INACTIVE_WORDS = [
  'موقوف', 'موقف', 'متوقف', 'غير نشط', 'ملغي', 'ملغى', 'مؤرشف', 'مؤرشفة',
  'inactive', 'archived', 'disabled', 'no', 'false', '0',
];

/**
 * يحوّل عمود «الحالة» في الشيت إلى `archived` منطقيّة.
 *
 * ⚠️ الفارغ ⇒ **غير مؤرشف** (نشط). الصنف الذي لم تُذكر حالته صنفٌ عامل — ولو
 * فسّرنا الفراغ إيقافًا لأوقف شيتٌ ناقصُ العمود الكتالوجَ كلّه دفعةً واحدة.
 * وما لا يُعرف يُحمل على النشاط للسبب ذاته: الإيقاف قرار صريح لا استنتاج.
 */
export function normalizeStatus(raw) {
  const s = String(raw ?? '').trim().toLowerCase();
  if (!s) return false;
  return INACTIVE_WORDS.includes(s);
}
